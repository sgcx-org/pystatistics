"""
Tests for ARIMA information-criterion correctness (RIGOR R18, 4.6.4).

The seasonal-ARIMA IC bug counted the coefficients of the expanded
multiplicative AR/MA polynomials (k=15 for the airline model) instead
of the free estimated parameters (k=3), silently inflating aic/aicc/bic
and making ``auto_arima`` select inferior seasonal models. A second,
coupled bug estimated a mean/intercept for differenced models (R
``stats::arima`` drops it when d + D > 0), which acted as an implicit
drift term.

Reference values generated by
``tests/fixtures/generate_arima_ic_r_reference.R`` (R 4.5.2).
"""

from __future__ import annotations

import json
from pathlib import Path

import numpy as np
import pytest
from numpy.testing import assert_allclose

from pystatistics.core.exceptions import ValidationError
from pystatistics.timeseries import arima, auto_arima

FIXTURE = Path(__file__).parent.parent / "fixtures" / "arima_ic_r_reference.json"

# Optimizer noise between our L-BFGS-B fits and R's optim(BFGS) is
# ~1e-4..5e-2 log-likelihood units on these models; the IC bug this
# guards against was a +24 AIC error, so 0.2 is both tight and robust.
LOGLIK_ATOL = 0.1
IC_ATOL = 0.2


@pytest.fixture(scope="module")
def ref():
    with open(FIXTURE) as f:
        return json.load(f)


@pytest.fixture(scope="module")
def airpassengers(ref):
    return np.asarray(ref["series"]["airpassengers"], dtype=np.float64)


@pytest.fixture(scope="module")
def fits(ref, airpassengers):
    """Fit every reference model once; reused across tests."""
    out = {}
    for label, m in ref["models"].items():
        seasonal = tuple(m["seasonal"]) if m.get("seasonal") else None
        fit = arima(
            airpassengers,
            order=tuple(m["order"]),
            seasonal=seasonal,
            method="CSS-ML",
        )
        out[label] = (fit, m)
    return out


# =========================================================================
# Normal cases: self-consistency and R parity
# =========================================================================

class TestICSelfConsistency:
    """ARIMASolution must satisfy .aic == -2*loglik + 2*n_params etc."""

    def test_aic_uses_n_params(self, fits):
        for label, (fit, _) in fits.items():
            expected = -2.0 * fit.log_likelihood + 2.0 * fit.n_params
            assert_allclose(fit.aic, expected, atol=1e-8, err_msg=label)

    def test_aicc_uses_n_params(self, fits):
        for label, (fit, _) in fits.items():
            k, n = fit.n_params, fit.n_used
            expected = fit.aic + 2.0 * k * (k + 1.0) / (n - k - 1.0)
            assert_allclose(fit.aicc, expected, atol=1e-8, err_msg=label)

    def test_bic_uses_n_params(self, fits):
        for label, (fit, _) in fits.items():
            expected = (
                -2.0 * fit.log_likelihood + fit.n_params * np.log(fit.n_used)
            )
            assert_allclose(fit.bic, expected, atol=1e-8, err_msg=label)


class TestRParity:
    """Free parameter counts and IC values match R stats::arima."""

    def test_n_params_matches_r_free_k(self, fits):
        # Exact integer equality: the airline model must count k=3
        # (ma1, sma1, sigma2), not the 15 of the expanded polynomial.
        for label, (fit, m) in fits.items():
            assert fit.n_params == m["k"], (
                f"{label}: n_params={fit.n_params}, R free k={m['k']}"
            )

    def test_n_used_matches_r_nobs(self, fits):
        for label, (fit, m) in fits.items():
            assert fit.n_used == m["nobs"], label

    def test_loglik_matches_r(self, fits):
        for label, (fit, m) in fits.items():
            assert_allclose(
                fit.log_likelihood, m["loglik"], atol=LOGLIK_ATOL,
                err_msg=label,
            )

    @pytest.mark.parametrize("ic", ["aic", "aicc", "bic"])
    def test_ic_matches_r(self, fits, ic):
        for label, (fit, m) in fits.items():
            assert_allclose(
                getattr(fit, ic), m[ic], atol=IC_ATOL, err_msg=label,
            )


class TestMeanHandlingUnderDifferencing:
    """R parity: include_mean is ignored when d + D > 0."""

    def test_mean_dropped_when_d_positive(self, fits):
        fit, _ = fits["ns211"]
        assert fit.mean is None

    def test_mean_dropped_when_seasonal_D_positive(self, fits):
        fit, _ = fits["airline_011_011_12"]
        assert fit.mean is None

    def test_explicit_include_mean_still_dropped(self, airpassengers):
        fit = arima(airpassengers, order=(1, 1, 0), include_mean=True)
        assert fit.mean is None
        # and the count must not include it: ar1 + sigma2
        assert fit.n_params == 2

    def test_mean_kept_without_differencing(self, fits):
        fit, m = fits["ns101_with_mean"]
        assert fit.mean is not None
        assert fit.n_params == m["k"]  # ar1, ma1, intercept, sigma2 = 4



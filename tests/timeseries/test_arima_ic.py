class TestValidation:
    def test_negative_max_P_raises(self, airpassengers):
        with pytest.raises(ValidationError):
            auto_arima(airpassengers, period=12, max_P=-1)

    def test_negative_max_Q_raises(self, airpassengers):
        with pytest.raises(ValidationError):
            auto_arima(airpassengers, period=12, max_Q=-1)

    def test_negative_max_D_raises(self, airpassengers):
        with pytest.raises(ValidationError):
            auto_arima(airpassengers, period=12, max_D=-1)

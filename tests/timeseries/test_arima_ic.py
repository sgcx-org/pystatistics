class TestDegenerateModels:
    """(0,d,0) models: only sigma2 is estimated (k = 1)."""

    @pytest.mark.parametrize(
        "label", ["ns010_degenerate", "s010_010_12_degenerate"],
    )
    def test_k_is_one(self, fits, label):
        fit, m = fits[label]
        assert fit.n_params == 1 == m["k"]

    @pytest.mark.parametrize(
        "label", ["ns010_degenerate", "s010_010_12_degenerate"],
    )
    def test_aicc_correction(self, fits, label):
        # k=1: AICc = AIC + 2k(k+1)/(n-k-1) = AIC + 4/(n-2).
        fit, _ = fits[label]
        assert_allclose(
            fit.aicc, fit.aic + 4.0 / (fit.n_used - 2), atol=1e-10,
        )

    @pytest.mark.parametrize(
        "label", ["ns010_degenerate", "s010_010_12_degenerate"],
    )
    def test_matches_r(self, fits, label):
        fit, m = fits[label]
        assert_allclose(fit.aic, m["aic"], atol=IC_ATOL)
        assert_allclose(fit.aicc, m["aicc"], atol=IC_ATOL)



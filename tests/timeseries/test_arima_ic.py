# auto_arima seasonal selection (the RIGOR R18 showstopper)
# =========================================================================

class TestAutoARIMASeasonalSelection:
    def test_matches_forecast_auto_arima(self, ref, airpassengers):
        """Default seasonal search must reproduce R's selection.

        Before the fix the inflated IC made auto_arima pick
        (2,1,2)(1,1,1)[12] — 7.5 AICc worse than R's choice by R's
        own (correct) accounting.
        """
        expected = ref["auto_arima_seasonal"]
        result = auto_arima(airpassengers, period=12, ic="aicc")
        assert result.best_order == tuple(expected["order"])
        assert result.best_seasonal == tuple(expected["seasonal"])
        assert_allclose(result.best_aic, expected["aicc"], atol=0.05)

    def test_search_visits_seasonal_orders(self, airpassengers):
        """The stepwise search must vary (P, Q), not pin them at (1, 1)."""
        result = auto_arima(airpassengers, period=12, ic="aicc")
        seasonal_orders = {
            s_order[:1] + s_order[2:3]
            for (_, s_order), _ in result.search_results
        }
        assert len(seasonal_orders) > 1

    def test_grid_search_covers_seasonal_grid(self, airpassengers):
        """stepwise=False iterates the full P/Q grid for seasonal models."""
        result = auto_arima(
            airpassengers, period=12, ic="aicc", stepwise=False,
            max_p=1, max_q=0, max_P=1, max_Q=1,
        )
        keys = {
            (order[0], order[2]) + s_order[:1] + s_order[2:3]
            for (order, s_order), _ in result.search_results
        }
        # 2 (p) * 1 (q) * 2 (P) * 2 (Q) distinct combinations
        assert len(keys) == 8
        assert result.best_seasonal is not None
        assert result.best_seasonal[3] == 12


class TestAutoARIMADifferencingChoice:
    def test_d_uses_kpss_like_forecast(self):
        """auto_arima's d comes from the KPSS test, as in R.

        forecast::auto.arima defaults to test='kpss'; the previous
        hardcoded test='adf' picked d=2 on WWWusage (over-differencing,
        R picks d=1) and d=0 on wineind (R: 1), sending the order
        search to the wrong model class entirely.
        """
        fixture = (
            Path(__file__).parent.parent
            / "fixtures" / "stationarity_r_reference.json"
        )
        with open(fixture) as f:
            www = np.asarray(
                json.load(f)["series"]["wwwusage"], dtype=np.float64,
            )
        result = auto_arima(www, period=1, ic="aicc")
        assert result.best_order[1] == 1


class TestWhittlePathMeanHandling:
    """The Whittle fast path shares the include_mean override and the
    free-parameter IC convention."""

    def test_mean_dropped_and_ic_consistent(self, airpassengers):
        fit = arima(airpassengers, order=(1, 1, 0), method="Whittle")
        assert fit.mean is None
        expected = -2.0 * fit.log_likelihood + 2.0 * fit.n_params
        assert_allclose(fit.aic, expected, atol=1e-8)
        assert fit.n_params == 2  # ar1 + sigma2



# ---------------------------------------------------------------------------
# Edge / failure cases (Rule 1: fail loud)
# ---------------------------------------------------------------------------

def test_xreg_wrong_length_raises(series):
    with pytest.raises(ValidationError, match="one row per observation"):
        arima(series["yA"], order=(1, 0, 0), xreg=series["x1"][:-3])


def test_fixed_unknown_name_raises(series):
    with pytest.raises(ValidationError, match="unknown coefficient name"):
        arima(series["e_arma"], order=(1, 0, 1), fixed={"ar9": 0})


def test_all_fixed_raises(series):
    with pytest.raises(ValidationError, match="nothing to"):
        arima(series["e_arma"], order=(1, 0, 0),
              fixed={"ar1": 0.5, "intercept": 0.0})


def test_drift_with_double_differencing_raises(series):
    with pytest.raises(ValidationError, match="drift regressor vanishes"):
        arima(series["yD"], order=(0, 2, 1), include_drift=True)


def test_newxreg_required_when_missing(series, fits):
    with pytest.raises(ValidationError, match="future regressor values"):
        forecast_arima(fits["A"], series["yA"], h=6)


def test_newxreg_wrong_shape_raises(series, fits):
    with pytest.raises(ValidationError, match="expected shape"):
        forecast_arima(fits["A"], series["yA"], h=6,
                       newxreg=np.zeros((6, 1)))


def test_whittle_with_xreg_raises(series):
    with pytest.raises(ValidationError, match="not supported with method='Whittle'"):
        arima(series["yA"], order=(1, 0, 0), xreg=series["x1"], method="Whittle")


def test_init_with_xreg_raises(series):
    with pytest.raises(ValidationError, match="not supported together with"):
        arima(series["yA"], order=(1, 0, 0), xreg=series["x1"],
              init=[0.5, 0.0, 0.0, 0.0])


def test_positional_fixed_vector_accepted(series):
    """R-style positional nan-vector is accepted alongside the dict form."""
    fit_dict = arima(series["e_arma"], order=(1, 0, 1), fixed={"ma1": 0})
    fit_vec = arima(series["e_arma"], order=(1, 0, 1),
                    fixed=[np.nan, 0.0, np.nan])
    assert_allclose(fit_dict.ar, fit_vec.ar, atol=1e-10)
    assert_allclose(fit_dict.ma, fit_vec.ma, atol=1e-10)


def test_xreg_forecast_roundtrip_no_regression_unaffected(series):
    """A plain (no-xreg) fit still forecasts identically (no regression path)."""
    fit = arima(series["yA"], order=(1, 0, 0))
    fc = forecast_arima(fit, series["yA"], h=5)
    assert fc.mean.shape == (5,)
    assert len(fit.xreg_coef) == 0

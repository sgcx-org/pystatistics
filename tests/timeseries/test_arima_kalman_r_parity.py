class TestSeasonalARLikelihood:
    """(1,1,1)(1,1,0)[12] fits must reproduce R's log-likelihood.

    Before the doubling-init fix these reported ~80 units below R.
    """

    @pytest.mark.parametrize("label", ["logap_111_110", "nottem_111_110"])
    def test_loglik_matches_r(self, fits, label):
        fit, m, _ = fits[label]
        assert_allclose(fit.log_likelihood, m["loglik"], atol=LOGLIK_ATOL)

    @pytest.mark.parametrize("label", ["logap_111_110", "nottem_111_110"])
    def test_coefficients_match_r(self, fits, label):
        fit, m, _ = fits[label]
        assert_allclose(fit.ar[0], m["coef"]["ar1"], atol=5e-3)
        assert_allclose(fit.ma[0], m["coef"]["ma1"], atol=5e-3)
        assert_allclose(fit.seasonal_ar[0], m["coef"]["sar1"], atol=5e-3)

    @pytest.mark.parametrize("label", ["logap_111_110", "nottem_111_110"])
    def test_aic_matches_r(self, fits, label):
        fit, m, _ = fits[label]
        assert_allclose(fit.aic, m["aic"], atol=2 * LOGLIK_ATOL)
        assert_allclose(fit.aicc, m["aicc"], atol=2 * LOGLIK_ATOL)


class TestStationaryInit:
    """The doubling iteration itself: normal, edge, failure."""

    def test_converges_for_persistent_seasonal_ar(self):
        # The exact failure case: expanded (1 - sar B^12) polynomial,
        # spectral radius ~0.94 — the old fixed-point gave ok=False.
        phi = np.zeros(13)
        phi[11] = -0.4731  # sar1 at lag 12 (expanded, 0-indexed lag 12)
        phi[12] = 0.0547 * 0.4731
        phi[0] = 0.0547
        R_vec = np.zeros(13)
        R_vec[0] = 1.0
        R_vec[1] = -0.4886
        P, ok = _stationary_init(phi, R_vec)
        assert ok
        # P must satisfy the Lyapunov equation P = T P T' + RR'.
        T = np.zeros((13, 13))
        T[:, 0] = phi
        for i in range(12):
            T[i, i + 1] = 1.0
        residual = T @ P @ T.T + np.outer(R_vec, R_vec) - P
        assert np.abs(residual).max() < 1e-10

    def test_agrees_with_scipy_lyapunov(self):
        from scipy.linalg import solve_discrete_lyapunov

        phi = np.array([0.5, -0.2, 0.05])
        R_vec = np.array([1.0, 0.3, 0.0])
        P, ok = _stationary_init(phi, R_vec)
        assert ok
        T = np.zeros((3, 3))
        T[:, 0] = phi
        T[0, 1] = T[1, 2] = 1.0
        P_ref = solve_discrete_lyapunov(T, np.outer(R_vec, R_vec))
        assert_allclose(P, P_ref, atol=1e-10)

    def test_white_noise_state(self):
        P, ok = _stationary_init(np.zeros(1), np.ones(1))
        assert ok
        assert_allclose(P, [[1.0]], atol=1e-14)

    def test_nonstationary_signals_failure(self):
        # Explosive AR: the doubling must NOT report success.
        P, ok = _stationary_init(np.array([1.5]), np.ones(1))
        assert not ok



FORECAST_LABELS = [
    "airline", "s211_010", "ns211", "ns010",
    "logap_111_110", "nottem_111_110",
    # d >= 2 / D >= 2: guard the _undifference integration order (the
    # (1,2,1) mean was off by four orders of magnitude at h=12).
    "d2_121", "d2_020", "D2_011_021",
]


class TestForecastRParity:
    @pytest.mark.parametrize("label", FORECAST_LABELS)
    def test_mean_matches_r(self, fits, label):
        fit, m, x = fits[label]
        fc = forecast_arima(fit, x, h=12)
        # Scale tolerance to the series (log-scale models are ~5.5).
        scale = max(1.0, float(np.mean(np.abs(x))) / 450.0)
        assert_allclose(
            fc.mean, m["fc_mean"], atol=FC_MEAN_ATOL * scale, err_msg=label,
        )

    @pytest.mark.parametrize("label", FORECAST_LABELS)
    def test_se_matches_r(self, fits, label):
        fit, m, x = fits[label]
        fc = forecast_arima(fit, x, h=12)
        # Relative: guards both the flat-se defect (rw: 3.5x off at
        # h=12) and the sigma2 defect (1.4% off).
        assert_allclose(
            fc.se, m["fc_se"], rtol=5e-3, err_msg=label,
        )

    def test_forecast_failure_is_loud(self, fits):
        # kalman_arma_forecast must raise, not return garbage, when the
        # filter fails at pathological parameters (non-finite AR poisons
        # the covariance propagation; the explosive-but-finite case is
        # self-stabilizing because the measurement is exact).
        from pystatistics.core.exceptions import ValidationError
        from pystatistics.timeseries._arima_kalman import (
            kalman_arma_forecast,
        )

        with pytest.raises(ValidationError):
            kalman_arma_forecast(
                np.ones(50), np.array([np.nan]), np.array([]), 5,
            )

    def test_random_walk_se_is_analytic(self, fits):
        # (0,1,0): se(h) = sigma * sqrt(h) exactly.
        fit, _, x = fits["ns010"]
        fc = forecast_arima(fit, x, h=8)
        expected = np.sqrt(fit.sigma2 * np.arange(1, 9))
        assert_allclose(fc.se, expected, rtol=1e-10)

    def test_se_increases_for_integrated_models(self, fits):
        fit, _, x = fits["ns211"]
        fc = forecast_arima(fit, x, h=12)
        assert np.all(np.diff(fc.se) > 0)



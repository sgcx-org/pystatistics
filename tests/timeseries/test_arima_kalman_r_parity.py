"""
R-parity tests for the ARIMA Kalman-path fixes (RIGOR R18 follow-up).

Three coupled defects, all confirmed against R 4.5.2:

1. The stationary initial covariance was solved by a linear fixed-point
   iteration with a 200-step budget and an absolute tolerance; a
   moderately persistent seasonal AR term (spectral radius ~0.94)
   failed the budget and silently fell back to the diffuse init,
   shifting the reported log-likelihood ~80 units below R on
   (1,1,1)(1,1,0)[12] models. Now solved by a doubling iteration.
2. Forecasts used the factored non-seasonal coefficients only (seasonal
   AR/MA dropped), seeded on CSS residuals (wrong near an MA unit
   root), and the SEs ignored differencing entirely (a random walk
   reported a flat se). Now forecast from the exact Kalman state, with
   the error covariance aggregated through the differencing operators.
3. vcov was computed over the expanded polynomial, so summary() read
   seasonal s.e. from structurally-zero lags. Now computed in the
   factored parameterization. sigma2 now reports the Kalman profile
   estimate for ML-family fits.

Reference values generated by
``tests/fixtures/generate_arima_kalman_r_reference.R`` (R 4.5.2).
"""

from __future__ import annotations

import json
from pathlib import Path

import numpy as np
import pytest
from numpy.testing import assert_allclose

from pystatistics.timeseries import arima
from pystatistics.timeseries._arima_forecast import forecast_arima
from pystatistics.timeseries._arima_kalman import _stationary_init

FIXTURE = Path(__file__).parent.parent / "fixtures" / "arima_kalman_r_reference.json"

# Optimizer noise bounds (measured deltas are ~1e-3; the defects being
# guarded against were 80 loglik units, 1.4-5.4 forecast units, and a
# 4.7x s.e. error).
LOGLIK_ATOL = 0.05
FC_MEAN_ATOL = 0.05
FC_SE_ATOL = 0.02
SE_ATOL = 2e-3


@pytest.fixture(scope="module")
def ref():
    with open(FIXTURE) as f:
        return json.load(f)


_SERIES_KEY = {
    "logap": "log_airpassengers",
    "nottem": "nottem",
    "co2": "co2",
}



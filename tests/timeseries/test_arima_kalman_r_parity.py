class TestConvergedFlag:
    def test_ml_success_overrides_css_failure(self, fits):
        # co2 (2,1,1): the CSS warm-start aborts on a NaN objective but
        # the ML refinement converges to R's optimum; the fit must not
        # raise and must match R.
        fit, m, _ = fits["co2_211"]
        assert fit.converged
        assert_allclose(fit.log_likelihood, m["loglik"], atol=LOGLIK_ATOL)


class TestSigma2AndSE:
    @pytest.mark.parametrize(
        "label", ["airline", "s211_010", "ns211", "logap_111_110"],
    )
    def test_sigma2_is_kalman_profile(self, fits, label):
        # The CSS-residual SSE/n overstated sigma2 by 2.9% on the
        # near-unit-MA model; R's profile estimate is the reference.
        fit, m, _ = fits[label]
        assert_allclose(fit.sigma2, m["sigma2"], rtol=5e-3, err_msg=label)

    @pytest.mark.parametrize("label", ["airline", "logap_111_110"])
    def test_seasonal_se_matches_r(self, fits, label):
        # vcov now lives in the factored parameterization: the s.e.
        # printed for sar/sma must be R's (airline sma1 was 4.7x off).
        fit, m, _ = fits[label]
        se = np.sqrt(np.abs(np.diag(fit.vcov)))
        names = []
        for i in range(len(fit.ar)):
            names.append(f"ar{i + 1}")
        for i in range(len(fit.ma)):
            names.append(f"ma{i + 1}")
        for i in range(len(fit.seasonal_ar)):
            names.append(f"sar{i + 1}")
        for i in range(len(fit.seasonal_ma)):
            names.append(f"sma{i + 1}")
        for idx, name in enumerate(names):
            assert_allclose(
                se[idx], m["se"][name], atol=SE_ATOL,
                err_msg=f"{label}:{name}",
            )

    def test_vcov_dimension_matches_coefficients(self, fits):
        # For seasonal fits vcov must be (p+q+P+Q) square, not the
        # expanded-polynomial size.
        fit, _, _ = fits["airline"]
        n_coef = (len(fit.ar) + len(fit.ma)
                  + len(fit.seasonal_ar) + len(fit.seasonal_ma))
        assert fit.vcov.shape == (n_coef, n_coef)



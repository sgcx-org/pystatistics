class TestNearUnitRootVeto:
    def test_boundary_ma_root_is_vetoed(self, fits):
        # nottem (1,1,1)(1,1,0)[12] fits with ma1 ~ -1.0 (root modulus
        # ~1.00001 < 1.01): forecast's myarima would set ic = Inf.
        from pystatistics.timeseries._arima_order import _has_near_unit_roots

        fit, _, _ = fits["nottem_111_110"]
        assert abs(fit.ma[0]) > 0.98  # precondition: boundary fit
        assert _has_near_unit_roots(fit)

    def test_interior_roots_survive(self, fits):
        # (2,1,1)(0,1,0)[12] has min MA root modulus ~1.018 > 1.01 —
        # R's own auto.arima pick, must NOT be vetoed.
        from pystatistics.timeseries._arima_order import _has_near_unit_roots

        fit, _, _ = fits["s211_010"]
        assert not _has_near_unit_roots(fit)

    def test_pure_differencing_survives(self, fits):
        # No AR/MA coefficients at all: nothing to veto.
        from pystatistics.timeseries._arima_order import _has_near_unit_roots

        fit, _, _ = fits["ns010"]
        assert not _has_near_unit_roots(fit)

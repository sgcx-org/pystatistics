"""
Tests for the RIGOR R18 stationarity-test fixes (4.6.4):

- adf_test defaults to regression='ct' (tseries::adf.test always uses
  constant + trend) and its statistic reproduces tseries exactly.
- ADF p-values use the MacKinnon (1994) surface and match statsmodels
  ``adfuller`` across nc/c/ct and the full p range — the old
  interpolation returned p=0.44 for a near-unit-root series where the
  correct value is ~0.92.
- kpss_test's default bandwidth follows tseries's lshort=TRUE rule
  trunc(4*(n/100)^(1/4)); at that (matched) bandwidth the statistic
  and interpolated p-value reproduce tseries::kpss.test.

Reference values generated by
``tests/fixtures/generate_stationarity_r_reference.R`` (R 4.5.2,
tseries 0.10-58) and
``tests/fixtures/generate_adf_statsmodels_reference.py``
(statsmodels 0.14.6).
"""


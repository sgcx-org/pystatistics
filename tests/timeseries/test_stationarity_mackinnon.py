class TestKPSSBandwidth:
    @pytest.mark.parametrize(
        "n,expected", [(50, 3), (100, 4), (200, 4), (400, 5), (1000, 7)],
    )
    def test_lshort_true_rule(self, n, expected, rng_series):
        assert kpss_test(rng_series(n)).n_lags == expected

    @pytest.mark.parametrize(
        "n,expected", [(100, 12), (200, 14), (400, 16)],
    )
    def test_lshort_false_rule(self, n, expected, rng_series):
        assert kpss_test(rng_series(n), lshort=False).n_lags == expected

    def test_explicit_n_lags_overrides_lshort(self, rng_series):
        x = rng_series(200)
        assert kpss_test(x, n_lags=7, lshort=False).n_lags == 7
        assert kpss_test(x, n_lags=0).n_lags == 0

    def test_lshort_must_be_bool(self, rng_series):
        with pytest.raises(ValidationError):
            kpss_test(rng_series(100), lshort="yes")


@pytest.fixture(scope="module")
def rng_series():
    def _make(n: int):
        return np.random.default_rng(7).normal(size=n)

    return _make


class TestKPSSDegenerateInput:
    def test_constant_series_raises(self):
        # 0/0 statistic: any returned value would be rounding noise.
        with pytest.raises(ValidationError):
            kpss_test(np.full(50, 5.0))

    def test_exactly_linear_series_raises_under_ct(self):
        with pytest.raises(ValidationError):
            kpss_test(np.arange(50, dtype=np.float64), regression="ct")

    def test_exactly_linear_series_ok_under_c(self):
        # A pure trend has real level-regression residuals; must work.
        result = kpss_test(np.arange(50, dtype=np.float64), regression="c")
        assert np.isfinite(result.statistic)


class TestNdiffsForecastParity:
    """ndiffs (default KPSS path) must match forecast::ndiffs.

    Guards the bandwidth pin: forecast::ndiffs uses
    use.lag = trunc(3*sqrt(n)/13), NOT kpss_test's tseries-style
    lshort default. WWWusage is the borderline case that separates
    the two (2 lags -> d=1, 4 lags -> d=0).
    """

    def test_matches_forecast_ndiffs(self, series, r_ref):
        for name, expected in r_ref["ndiffs_kpss"].items():
            assert ndiffs(series[name]) == expected, name


class TestKPSSTseriesParity:
    def test_level(self, series, r_ref):
        for name, x in series.items():
            expected = r_ref["reference"][name]
            result = kpss_test(x, regression="c")
            assert result.n_lags == expected["kpss_level_lag"], name
            assert_allclose(
                result.statistic, expected["kpss_level_stat"], atol=1e-8,
                err_msg=name,
            )
            assert_allclose(
                result.p_value, expected["kpss_level_p"], atol=1e-6,
                err_msg=name,
            )

    def test_trend(self, series, r_ref):
        for name, x in series.items():
            expected = r_ref["reference"][name]
            result = kpss_test(x, regression="ct")
            assert result.n_lags == expected["kpss_trend_lag"], name
            assert_allclose(
                result.statistic, expected["kpss_trend_stat"], atol=1e-8,
                err_msg=name,
            )
            assert_allclose(
                result.p_value, expected["kpss_trend_p"], atol=1e-6,
                err_msg=name,
            )
